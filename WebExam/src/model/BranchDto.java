package model;

public class BranchDto {
	int branch_no;
	String branch_name;
	String branch_location;
	String branch_tel;
	String branch_convenience;
	public int getBranch_no() {
		return branch_no;
	}
	public void setBranch_no(int branch_no) {
		this.branch_no = branch_no;
	}
	public String getBranch_name() {
		return branch_name;
	}
	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}
	public String getBranch_location() {
		return branch_location;
	}
	public void setBranch_location(String branch_location) {
		this.branch_location = branch_location;
	}
	public String getBranch_tel() {
		return branch_tel;
	}
	public void setBranch_tel(String branch_tel) {
		this.branch_tel = branch_tel;
	}
	public String getBranch_convenience() {
		return branch_convenience;
	}
	public void setBranch_convenience(String branch_convenience) {
		this.branch_convenience = branch_convenience;
	}
	
	

}
